function fadeImage1(){
    document.getElementById("Imagem1").style.animation = "sumir 0.5s linear"
    document.getElementById("texto1").style.animation = "aparecer 0.5s linear"
    setTimeout(() => document.getElementById("Imagem1").style.opacity = "0%", 500);
    setTimeout(() => document.getElementById("texto1").style.opacity = "100%", 500);
}
